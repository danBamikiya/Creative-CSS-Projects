function openNav_right() {
	document.getElementById("Nav_right").style.width = "100%";
} //  Slides in the overlay to the right when someone clicks on the span element

function closeNav_right() {
	document.getElementById("Nav_right").style.width = "0%";
} //  Slides out the overlay from the right when someone clicks on the close button

function openNav_down() {
	document.getElementById("Nav_down").style.height = "100%";
} //  Slides down the overlay when someone clicks on it

function closeNav_down() {
	document.getElementById("Nav_down").style.height = "0%";
} //  Slides up the overlay when someone clicks it
